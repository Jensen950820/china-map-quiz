def process_file(province):
    fpath = os.path.join(BASE, province, 'gd_data.js')
    print(f'处理: {fpath}')
    with open(fpath, 'r', encoding='utf-8') as f:
        content = f.read()
    # 提取JSON
    m = re.search(r'=\s*(\{.*\})\s*;?\s*$', content, re.DOTALL)
    if not m:
        print(f'  ✗ 解析 {province} 失败')
        return
    data = json.loads(m.group(1))
    features = data.get('features', [])
    updated = 0
    for feat in features:
        props = feat.get('properties', {})
        name = props.get('name', '')
        if name in DESCS and 'desc' not in props:
            props['desc'] = DESCS[name]
            updated += 1
            print(f'  + {name}')
        elif 'desc' in props:
            print(f'  · {name} (已有desc，跳过)')
        else:
            print(f'  ? {name} (无对应简介)')
    # 写回
    new_json = json.dumps(data, ensure_ascii=False, indent=2)
    # 保留原始前缀
    prefix = content[:content.index('=')+1].rstrip() + '\n'
    with open(fpath, 'w', encoding='utf-8') as f:
        f.write(prefix + new_json + '\n')
    print(f'  → {province} 完成，更新 {updated} 条\n')

for prov in ['辽宁', '吉林', '黑龙江']:
    process_file(prov)
print('全部完成！')
